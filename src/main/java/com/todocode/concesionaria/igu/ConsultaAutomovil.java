    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Control de tabla con automoviles
        if(tablaAutos.getRowCount()>0){
            if(tablaAutos.getSelectedRow()!=-1){
                //Obtener ID del auto a borrar
                int idAuto = Integer.parseInt( String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                controladora.borrarAuto(idAuto);
                mostrarMensaje("El automovil ha sido borrado", "Info", "Borrado Exitoso");
                //Recarga de la tabla
                cargarTabla();
            }else{
                mostrarMensaje("Seleccione un registro", "Error", "Error al eliminar");
            }
        }else{
            mostrarMensaje("La tabla esta vacia, no se puede eliminar", "Error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //Control de tabla con automoviles
        if(tablaAutos.getRowCount()>0){
            if(tablaAutos.getSelectedRow()!=-1){
               //Obtener ID del auto a borrar
                int idAuto = Integer.parseInt( String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                
                ModificarAutomovil modificarAutomovil = new ModificarAutomovil(idAuto);
                modificarAutomovil.setVisible(true);
                modificarAutomovil.setLocationRelativeTo(null);
                this.dispose();
                
            }else{
                mostrarMensaje("Seleccione un registro", "Error", "Error al modificar");
            }
        }else{
            mostrarMensaje("La tabla esta vacia, no se puede modificar", "Error", "Error al modificar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed


